void consultarCorreos(){
  String consulta = "SELECT correo,password FROM usuarios WHERE correo=?";
  String correo = txt_correo.getText();
  try {
    ps = (PreparedStatement) conexion.conectar.prepareStatement(consulta);
    ps.setString(1, correo);
    rs = ps.executeQuery();
    if (rs.next()) {
      String resultado = rs.getString(1);
      String pas=rs.getString(2);
      //compruebo si el correo ingresado por el usuario existe en la base de datos
      if(resultado.equalsIgnoreCase(correo)){
          // El correo ingresado por el usuario coincide con uno en la base de datos
           JOptionPane.showMessageDialog(this,"El correo "+resultado+" ha sido encontrado en la base de datos");
            EjecutarResendApiPython(resultado,pas);
      }
      else{
          JOptionPane.showMessageDialog(this,"No se ha encontrado el correo especificado, por favor confrme y vuela a intentar");
      }
    } else {
      JOptionPane.showMessageDialog(this,"No se encontraron resultados para el correo especificado");
    }
  } catch (SQLException e) {
    System.out.println("Error: " + e);
  }
}

